package model;

/**
 * The Direction enum represents the eight possible directions a piece can move
 * on the chess board. These directions are used to define movement patterns
 * for different pieces.
 *
 * Design Pattern: Enum
 * Role: Enumeration - Defines a fixed set of constants for movement directions.
 *
 * @author Tai Zhi Xuan
 */
public enum Direction {
    UP, DOWN, LEFT, RIGHT, UP_LEFT, UP_RIGHT, DOWN_LEFT, DOWN_RIGHT;
}
